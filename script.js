// Code inspired by Barney Codes -- https://www.youtube.com/@BarneyCodes -- [Stacker Game]
function updateBlock() {
    currentBlock.x += blockDir * blockSpeed;

    if (currentBlock.x < 0) {
        blockDir = 1;
    }
    if (currentBlock.x + currentBlock.z > width) {
        blockDir = -1;
    }

}

